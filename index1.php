							<img src="images/people.png" alt="" class="imgleft" /><p>Forced to be a programmer by love and not by choice, I am discovering that there is an incredible world outside of Microsoft technologies that needs exploring!</p>

<p>I have been a .Net programmer for 8+ years. At the end of High School with no real clue what I was going to do with my life, programing called to me. I am a country boy who didn't even get the internet until I was 17 years old. The first day I had it, I learned how to make my own website via howstuffworks.com and less than a year later was enrolled in college to get a Degree in Software Development. I spent one year at a "normal" college and realized it was wasting my time re-learning History and Science and NOT learning how to program, so I finished my Associates Degree at a Technical School.</p>

<p>I started work as a VB.Net Developer 7 months after .Net came off the shelves and have since worked in VB.Net and C# in Desktop and ASP.Net applications.</p>

<p>4 years ago I purchased a Macbook Pro with the intent of booting it into Windows XP and have not looked back since. The discovery of OS X has led me to reexamine my entire outlook on development.</p>

<p>Technologies such as Objective-C, Ruby, Python, PHP, jQuery, CSS and countless others have massively expanded my horizons. In the years since opening my eyes I have published an iPhone application, built multiple Ruby scripts, developed sites in PHP using MySQL and explored more technologies than I can remember.</p>

<p>I am at a point in my life where I find myself NEEDING to work with inspired people who want to change the world.</p>
